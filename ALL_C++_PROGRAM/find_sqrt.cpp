// this program for find the square root of number.
#include<iostream>
#include<cmath> //add extra <cmath> fire.
using namespace std;
int main()
{
    float sq,n;
    cout<<"Enter any positive number :: ";
    cin>>n;
    sq=sqrt(n);
    cout<<"\n Square root of Entered number["<<n<<"] is ::"<<sq<<"\n";
    return 0;

}